package com.kn.printPatterns;

public class Pattern10 {

	public static void main(String[] args) {
		for(int i=1; i<=5; i++) {
			for(int j=1; j<=5; j++) {
				System.out.print((6-j)+" ");
		}
			System.out.println();
		}

	}
}
//i=row, j=column
